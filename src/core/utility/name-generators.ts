export interface INameGenerator {
    getName(context: any) : string;
}

export const getCamelCasedName = (buf: string): string => {

    if (!buf)
        return buf;

    // strip non-alpha
    buf = buf.replace(/[^a-z0-9 ]+/gi, ' ');

    const parts = buf.split(/[ -]+/);
    let result = "";

    for (const part of parts) {
        if (part && part.length > 0 && part != " ")
            result += (part.substr(0, 1).toUpperCase() + part.substr(1).toLowerCase());
    }

    return result;
};

export const getRandomName = (prefix: string = "", suffix: string = "", delimiter: string = "-"): string => {
    return `${prefix}${adjective[random.int(0, adjective.length - 1)]}${delimiter}${noun[random.int(0, noun.length - 1)]}${suffix}`;
};
